// Q1: Delayed Greeting

// Create a function called delayedHello that prints "Hello, welcome to JavaScript!" after 2 seconds using setTimeout.

function delayedHello() {
  setTimeout(() => {
    console.log("Hello, welcome to JavaScript!");
  }, 2000);
}
delayedHello();

